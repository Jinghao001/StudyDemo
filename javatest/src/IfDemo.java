public class IfDemo {
    public static void main(String[] args) {
        int x=3;
        if (x>1){
            System.out.println("x大于1");
        }
        else if (x<3){
            System.out.println("x小于3");
        }
        else if (x==3) {
            System.out.println("x等于3");
        }
    }
}
